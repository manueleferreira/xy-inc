CREATE TABLE BUSINESS_DOMAIN_ATT (
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
	NAME VARCHAR(255) NOT NULL,
	BUSINESS_DOMAIN_ID BIGINT NOT NULL,
    TYPE VARCHAR(255) NOT NULL
);

CREATE TABLE BUSINESS_DOMAIN_INSTANCE (
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
	BUSINESS_DOMAIN_ID BIGINT NOT NULL
);

CREATE TABLE BUSINESS_DOMAIN_INSTANCE_ATT (
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
	BUSINESS_DOMAIN_INSTANCE_ID BIGINT NOT NULL,
	BUSINESS_DOMAIN_ATT_ID BIGINT NOT NULL,
	ATT_VALUE VARCHAR(255) NOT NULL
);
